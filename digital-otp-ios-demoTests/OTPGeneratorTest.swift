struct OTPGenerator {
    var timeStamp: Int
    var sharedKey: String
    var otpExpected: String
    var question: String
    var password: String
    
    let ocraSuite = "OCRA-1:HOTP-SHA256-6:QA06-PSHA256-T30S"
    
    func generator() -> OCRABuilderInterface {
        return OCRAGenerator().accept(suite: ocraSuite, key: sharedKey)
            .params(counter: "", question: question, password: password)
            .sessionWith(sessionInfo: "", timestamp: timeStamp)
    }
}

